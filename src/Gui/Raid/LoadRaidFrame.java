    private void getRaid(){
        try {
            String str = (String)dateList.getSelectedValue();
            Date date = Cader.showDateFormat.parse(str);
            this.raid =preshowHandler.readRaid(date);
            drawPreShow();
        } catch (ParseException ex) {
        }
    }
    
    
    private void drawPreShow(){
        String line=Cader.showDateFormat.format(raid.getTimestamp());
        dateLabel.setText(line);
        String[] names = raid.getMembersNames();
        int countTLG=0;
        int countERS=0;
        for(int i=0;i<names.length;i++){
            switch(raid.getMemberByName(names[i]).getState()){
                    case TEILG: 
                        countTLG++;
                        break;
                    case ERSATZ:
                        countERS++;
                        break;
            }
        }
        memberLabel1.setText(countTLG+"//"+names.length);
        memberLabel2.setText(countERS+"//"+names.length);
        lootLabel.setText(""+raid.getlootDates().length);
    }
