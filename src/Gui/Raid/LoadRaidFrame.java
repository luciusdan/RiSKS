    public void open(){
        LinkedList<Date> dates = preshowHandler.getRaidDates();
        String[] listData = new String[dates.size()];
        
        for(int i=0;i<dates.size();i++){
            listData[i] = Cader.showDateFormat.format(dates.get(i));
        }
        if(dateList.getFirstVisibleIndex()>=0){
            dateList.setListData(listData);
            dateList.setSelectedIndex(0);
            getRaid();
        }
        
        setVisible(true);
    }
